/**
 * Homepage for the admin app
 */
@AppPage(EmrConstants.APP_ADMIN)
public class ExportHomePageController {

	public void controller() {
	}
	
	@AppAction(EmrConstants.APP_ADMIN)
	public SimpleObject submit(UiUtils ui) 
	{		
		try {
			XSSFWorkbook workbook = new XSSFWorkbook();
	        XSSFSheet sheet = workbook.createSheet("Java Books");
	         
	        Object[][] bookData = {
	                {"Head First Java", "Kathy Serria", 79},
	                {"Effective Java", "Joshua Bloch", 36},
	                {"Clean Code", "Robert martin", 42},
	                {"Thinking in Java", "Bruce Eckel", 35},
	        };
	        int rowCount = 0;
	         
	        for (Object[] aBook : bookData) {
	            Row row = sheet.createRow(++rowCount);
	             
	            int columnCount = 0;
	             
	            for (Object field : aBook) {
	                Cell cell = row.createCell(++columnCount);
	                if (field instanceof String) {
	                    cell.setCellValue((String) field);
	                } else if (field instanceof Integer) {
	                    cell.setCellValue((Integer) field);
	                }
	            }
	             
	        }
	         
	        FileOutputStream outputStream = new FileOutputStream("JavaBooks.xlsx");
            workbook.write(outputStream);
        

            return SimpleObject.create("success", "1");
		}
		catch(Exception e)
		{
			return SimpleObject.create("success", "0");
		}
		finally {
			// Ensure that all proxy privileges are removed
			
		}
		
		//return SimpleObject.create("success", "1");
	}
}